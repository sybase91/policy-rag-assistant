def agent(
    id_: str,
    category: str,
    turns: list[str],
    *,
    expected_answer_type: str | None = None,
    expected_decision: str | list[str] | None = None,
    expected_final_decision: str | list[str] | None = None,
    expected_policy_area: str | None = None,
    expected_required_approvals_any: list[str] | None = None,
    expected_sections_any: list[str] | None = None,
    must_include_concepts: list[str] | None = None,
    should_not_include: list[str] | None = None,
    failure_modes_to_check: list[str] | None = None,
    facts_that_should_not_be_asked_again: list[str] | None = None,
    mock_sections: list[str] | None = None,
) -> dict:
    case = {
        "id": id_,
        "category": category,
        "mode": "policyops_agent",
        "turns": turns,
    }
    if expected_answer_type:
        case["expected_answer_type"] = expected_answer_type
    if expected_decision is not None:
        case["expected_decision"] = expected_decision
    if expected_final_decision is not None:
        case["expected_final_decision"] = expected_final_decision
    if expected_policy_area:
        case["expected_policy_area"] = expected_policy_area
    if expected_required_approvals_any:
        case["expected_required_approvals_any"] = expected_required_approvals_any
    if expected_sections_any:
        case["expected_sections_any"] = expected_sections_any
    if must_include_concepts:
        case["must_include_concepts"] = must_include_concepts
    if should_not_include:
        case["should_not_include"] = should_not_include
    if failure_modes_to_check:
        case["failure_modes_to_check"] = failure_modes_to_check
    if facts_that_should_not_be_asked_again:
        case["facts_that_should_not_be_asked_again"] = facts_that_should_not_be_asked_again
    if mock_sections:
        case["mock_sections"] = mock_sections
    return case


